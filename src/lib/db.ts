import mongoose from 'mongoose';

/**
 * PRODUCTION NOTE:
 * For the initial client demo, the UI uses hardcoded mock data so this DB connection 
 * is not strictly required to browse the site. 
 * Before the final live launch, ensure MONGODB_URI is set in Vercel.
 */

const MONGODB_URI = process.env.MONGODB_URI || 'mongodb://localhost:27017/manevia';

if (!MONGODB_URI) {
  throw new Error('Please define the MONGODB_URI environment variable inside .env.local');
}

/**
 * Global is used here to maintain a cached connection across hot reloads
 * in development. This prevents connections growing exponentially
 * during API Route usage.
 */
let cached = (global as any).mongoose;

if (!cached) {
  cached = (global as any).mongoose = { conn: null, promise: null };
}

async function dbConnect() {
  if (cached.conn) {
    return cached.conn;
  }

  if (!cached.promise) {
    const opts = {
      bufferCommands: false,
    };

    cached.promise = mongoose.connect(MONGODB_URI, opts).then((mongoose) => {
      return mongoose;
    });
  }
  
  try {
    cached.conn = await cached.promise;
  } catch (e) {
    cached.promise = null;
    throw e;
  }

  return cached.conn;
}

export default dbConnect;
